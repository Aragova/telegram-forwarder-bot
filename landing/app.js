const switcher = document.getElementById('langSwitcher');
const burger = document.querySelector('[data-burger]');
const nav = document.querySelector('.nav');

function applyLanguage(lang) {
  const dictionary = translations[lang] || translations.ru;
  document.documentElement.lang = lang;
  document.querySelectorAll('[data-i18n]').forEach((node) => {
    const value = dictionary[node.dataset.i18n];
    if (value !== undefined) node.textContent = value;
  });
  document.querySelectorAll('[data-i18n-html]').forEach((node) => {
    const value = dictionary[node.dataset.i18nHtml];
    if (value !== undefined) node.innerHTML = value;
  });
  localStorage.setItem('vimi_lang', lang);
  if (switcher) switcher.value = lang;
}

switcher?.addEventListener('change', (event) => applyLanguage(event.target.value));
burger?.addEventListener('click', () => nav?.classList.toggle('open'));
nav?.querySelectorAll('a').forEach((a) => a.addEventListener('click', () => nav?.classList.remove('open')));
applyLanguage(localStorage.getItem('vimi_lang') || 'ru');
